package com.ssh.model;

public class ProductSuit {
	private int id;
	private int Product_code;
	private String Product_name;
	private String Release_time;
	private float Market_price;
	private float Member_price;
	private float Product_tax;
	private int Quantity;
	private int Product_weight;
	private String Product_until;
	private String Start_time;
	private String End_time;
	private int Integral;
	private int Discount_coupon;
	public ProductSuit(){
		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getProduct_code() {
		return Product_code;
	}
	public void setProduct_code(int product_code) {
		Product_code = product_code;
	}
	public String getProduct_name() {
		return Product_name;
	}
	public void setProduct_name(String product_name) {
		Product_name = product_name;
	}
	public String getRelease_time() {
		return Release_time;
	}
	public void setRelease_time(String release_time) {
		Release_time = release_time;
	}
	public float getMarket_price() {
		return Market_price;
	}
	public void setMarket_price(float market_price) {
		Market_price = market_price;
	}
	public float getMember_price() {
		return Member_price;
	}
	public void setMember_price(float member_price) {
		Member_price = member_price;
	}
	public float getProduct_tax() {
		return Product_tax;
	}
	public void setProduct_tax(float product_tax) {
		Product_tax = product_tax;
	}
	public int getQuantity() {
		return Quantity;
	}
	public void setQuantity(int quantity) {
		Quantity = quantity;
	}
	public int getProduct_weight() {
		return Product_weight;
	}
	public void setProduct_weight(int product_weight) {
		Product_weight = product_weight;
	}
	public String getProduct_until() {
		return Product_until;
	}
	public void setProduct_until(String product_until) {
		Product_until = product_until;
	}
	public String getStart_time() {
		return Start_time;
	}
	public void setStart_time(String start_time) {
		Start_time = start_time;
	}
	public String getEnd_time() {
		return End_time;
	}
	public void setEnd_time(String end_time) {
		End_time = end_time;
	}
	public int getIntegral() {
		return Integral;
	}
	public void setIntegral(int integral) {
		Integral = integral;
	}
	public int getDiscount_coupon() {
		return Discount_coupon;
	}
	public void setDiscount_coupon(int discount_coupon) {
		Discount_coupon = discount_coupon;
	}
	
}
