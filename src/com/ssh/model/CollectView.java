package com.ssh.model;

public class CollectView {
	private String Mobile_phone;
	private int CodeType;
	private String Product_name;
	private float Member_price;
	private String Product_photo;
	private Boolean Product_state;
	private int Product_code;
	private String Subclass_name;
	private int id;
	public CollectView(){
		
	}
	public String getMobile_phone() {
		return Mobile_phone;
	}
	public void setMobile_phone(String mobile_phone) {
		Mobile_phone = mobile_phone;
	}
	public int getCodeType() {
		return CodeType;
	}
	public void setCodeType(int codeType) {
		CodeType = codeType;
	}
	public String getProduct_name() {
		return Product_name;
	}
	public void setProduct_name(String product_name) {
		Product_name = product_name;
	}
	public float getMember_price() {
		return Member_price;
	}
	public void setMember_price(float member_price) {
		Member_price = member_price;
	}
	public String getProduct_photo() {
		return Product_photo;
	}
	public void setProduct_photo(String product_photo) {
		Product_photo = product_photo;
	}
	public Boolean getProduct_state() {
		return Product_state;
	}
	public void setProduct_state(Boolean product_state) {
		Product_state = product_state;
	}
	public int getProduct_code() {
		return Product_code;
	}
	public void setProduct_code(int product_code) {
		Product_code = product_code;
	}
	public String getSubclass_name() {
		return Subclass_name;
	}
	public void setSubclass_name(String subclass_name) {
		Subclass_name = subclass_name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
