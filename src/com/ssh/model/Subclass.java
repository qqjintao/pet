package com.ssh.model;

public class Subclass {
	private int id;
	private int Subclass_code;
	private String Subclass_name;
	private int BroadHeading_code;
	public Subclass(){
		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSubclass_code() {
		return Subclass_code;
	}
	public void setSubclass_code(int subclass_code) {
		Subclass_code = subclass_code;
	}
	public String getSubclass_name() {
		return Subclass_name;
	}
	public void setSubclass_name(String subclass_name) {
		Subclass_name = subclass_name;
	}
	public int getBroadHeading_code() {
		return BroadHeading_code;
	}
	public void setBroadHeading_code(int broadHeading_code) {
		BroadHeading_code = broadHeading_code;
	}
	
}
