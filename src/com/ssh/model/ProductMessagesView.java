package com.ssh.model;

public class ProductMessagesView {
	private String Head_portrait;
	private String Nickname;
	private int ProductMessages_code;
	private int Product_code;
	private String ProductMessages_title;
	private String ProductMessages_context;
	private String ProductMessages_time;
	public ProductMessagesView(){
		
	}
	public String getHead_portrait() {
		return Head_portrait;
	}
	public void setHead_portrait(String head_portrait) {
		Head_portrait = head_portrait;
	}
	public String getNickname() {
		return Nickname;
	}
	public void setNickname(String nickname) {
		Nickname = nickname;
	}
	public int getProductMessages_code() {
		return ProductMessages_code;
	}
	public void setProductMessages_code(int productMessages_code) {
		ProductMessages_code = productMessages_code;
	}
	public int getProduct_code() {
		return Product_code;
	}
	public void setProduct_code(int product_code) {
		Product_code = product_code;
	}
	public String getProductMessages_title() {
		return ProductMessages_title;
	}
	public void setProductMessages_title(String productMessages_title) {
		ProductMessages_title = productMessages_title;
	}
	public String getProductMessages_context() {
		return ProductMessages_context;
	}
	public void setProductMessages_context(String productMessages_context) {
		ProductMessages_context = productMessages_context;
	}
	public String getProductMessages_time() {
		return ProductMessages_time;
	}
	public void setProductMessages_time(String productMessages_time) {
		ProductMessages_time = productMessages_time;
	}
	
}
